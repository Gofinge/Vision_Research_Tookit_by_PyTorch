    FACTORY = {"voc": "PascalVOCDataset"}

    @staticmethod
    def get(name):
        if "voc" in name:
            data_dir = DatasetCatalog.DATA_DIR
            args = DatasetCatalog.DATASETS[name]
            args["data_dir"] = os.path.join(data_dir, args["data_dir"])
            return dict(
                name='voc',     # file name in data/datasets
                args=args,
            )
